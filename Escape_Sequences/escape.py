# prints the text after the escape sequence on the next line 
print("My name is Aditya \nI am a computer engineer")

# prints backslash when using \\
print("Life is like a icecream\\taste it, before it melts")

# \t prints the string with horizontal tab space 
print("First\tSecond\tThird")
print("1\t\t2\t\t3")
print("1\t\t2\t\t3")
# prints the \" double qoute if user wants a double quote 
print("In last november i visited \"Hyderabad\"")

# prints the \' single qoute if user wants a single quote in the string
print("In last november i visited \'Hyderabad\'")